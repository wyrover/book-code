int OutputFileByBA(char *filename, unsigned char *ba, int ba_len)
{
    FILE *fpout;
    int i = 0;

    if ((fpout = fopen(filename, "wb")) == NULL)
        return 0;

    for (i = 0; i < ba_len; i++)
        fputc(ba[i], fpout);

    fclose(fpout);
    return 1;
}

int SaveText(char *filename, char *string)
{
    FILE *fp;

    if ((fp = fopen(filename, "wt")) == NULL)
        return 0;

    fwrite(string, 1, strlen(string), fp);
    fclose(fp);
    return 1;
}

